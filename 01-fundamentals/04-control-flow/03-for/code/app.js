// For

// Display "Hello World" five times in the console
for (let index = 0; index < 5; index++) {
  console.log("Hello World! ", index);
}

console.log("---------");

// Display odd number between 1 to 5
for (let i = 1; i <= 5; i++) {
  if (i % 2 !== 0) console.log(i);
}

console.log("---------");

// Display odd number between 1 to 5 in reverse order
for (let i = 5; i >= 1; i--) {
  if (i % 2 !== 0) console.log(i);
}
