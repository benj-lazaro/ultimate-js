// Equality Operators

// Strict Equality
console.log(1 === 1);
console.log("1" === 1); // Returns false due to the difference of type
console.log(true === 1);

// Lose Equality
console.log(1 == 1);
console.log("1" == 1); // Returns true due to the equality of value alone
console.log(true == 1); // Converts the right side into a boolean w/ 1 being equal to true
