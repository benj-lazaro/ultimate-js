// Comparison Operators

let x = 1;

// Relational Operators
// Greater than
console.log(x > 0);

// Greater than or equal to
console.log(x >= 1);

// Less than
console.log(x < 1);

// Less than or equal to
console.log(x <= 1);
console.log("----------------");

// Equality Operators
// Equal to
console.log(x === 1);

// Not equal to
console.log(x !== 1);
console.log("----------------");
