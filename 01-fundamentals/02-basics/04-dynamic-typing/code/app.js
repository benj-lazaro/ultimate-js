// Dynamic Typing

// Declare a variable with a string literal
let userName = "Mosh";
console.log(typeof userName);

// Dynamically change the value of the variable
userName = 1;
console.log(typeof userName);

let isApproved = true;
console.log(typeof isApproved);

// Assign the value of "undefined" (which is also a type) to variable firstName
let firstName = undefined;
console.log(typeof firstName);

// Returns an "object" type despite assigning a "null" value
let selectedColor = null;
console.log(typeof selectedColor);
